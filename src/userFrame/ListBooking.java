    public void LoadAllDataIntoTable(List<userFrameBean> list){
       DefaultTableModel dtm = (DefaultTableModel) jTable.getModel();
       dtm.setRowCount(0);
       
       NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("id","ID"));
       DecimalFormat df = (DecimalFormat) nf;
       df.setDecimalSeparatorAlwaysShown(false);
       df.setMaximumFractionDigits(0);
               
       for(userFrameBean bean : list){
           Vector v = new Vector();
           v.add(bean.getBookingId());
           v.add(bean.getLapanganId());
           v.add(bean.getNamaTeam());
           v.add(bean.getTanggalBooking());
           
           String jam = bean.getStartTime().toString() + " - " + bean.getEndTime().toString();
           
           v.add(jam);
           String formatHarga = nf.format(bean.getTotalHarga());
           v.add(formatHarga);
           
           dtm.addRow(v);
       }
    }
    
    public void loadTable(){
        UserFrameBal bal = new UserFrameBal();
        List list = bal.loadTableUserFrame(userId);
        LoadAllDataIntoTable(list);
    }

