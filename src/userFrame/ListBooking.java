    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (selectedBookingId != -1) {
        UserFrameBal ufb = new UserFrameBal();
        boolean success = ufb.deleteBooking(selectedBookingId, userId);
        
        
        if (success) {
            loadTable();
            
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus booking.");
        }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih di table booking yang ingin dihapus.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

